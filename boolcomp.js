// boolean and comparison operator

// booleans -> True or false


let n1 = 10;
let n2 = 12;

console.log(n1>n2);
console.log(n1<n2);
console.log(n1>=n2);
console.log(n1<=n2);
console.log(n1 == n2);



// == vs === -> In == it will check the value but in === it will check the datatype with value

console.log("== vs ===");

let num1 = '22';
let num2 = '22';

console.log(num1 == num2);
console.log(num1 == num2);
console.log(num1 === num2);


// != vs !==
console.log("!= vs !==");
console.log(num1 != num2);

